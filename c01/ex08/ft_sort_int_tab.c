void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int j;
	int k;

	i = size;
	while (i > 1)
	{
		j = 1;
		while (j < i)
		{
			if (tab[j - 1] > tab[j])
			{
				k = tab[j - 1];
				tab[j - 1] = tab[j];
				tab[j] = k;
			}
			j++;
		}
		i--;
	}
}
